package longtt.dtos;

import java.io.Serializable;

/**
 *
 * @author Long
 */
public class AreaDTO implements Serializable {
    String name;
    int areaId;

    public AreaDTO() {
    }

    public AreaDTO(int areaId, String name) {
        this.areaId = areaId;
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAreaId() {
        return areaId;
    }

    public void setAreaId(int areaId) {
        this.areaId = areaId;
    }
}
